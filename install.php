    ['GD Extension', extension_loaded('gd'), extension_loaded('gd') ? 'Loaded' : 'Missing (optional)'],
    ['cURL Extension', extension_loaded('curl'), extension_loaded('curl') ? 'Loaded' : 'Missing'],
    ['JSON Extension', extension_loaded('json'), extension_loaded('json') ? 'Loaded' : 'Missing'],
    ['mbstring Extension', extension_loaded('mbstring'), extension_loaded('mbstring') ? 'Loaded' : 'Missing'],
    ['config.php writable', is_writable(__DIR__) || is_writable(__DIR__ . '/config.php'), is_writable(__DIR__) ? 'Yes' : 'No — check permissions'],
    ['uploads/ writable', is_writable(__DIR__ . '/public/uploads') || !is_dir(__DIR__ . '/public/uploads'), is_writable(__DIR__ . '/public/uploads') ? 'Yes' : 'Create /public/uploads/'],
    ['mod_rewrite', function_exists('apache_get_modules') ? in_array('mod_rewrite', apache_get_modules()) : true, 'Assumed OK'],
];
$allPassed = array_reduce($requirements, fn($carry, $r) => $carry && $r[1], true);
