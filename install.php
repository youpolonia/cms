<?php
/**
 * Jessie CMS Installer
 * Web-based installation wizard
 * Delete this file after installation!
 */

// Prevent running if already installed
if (file_exists(__DIR__ . '/config.php')) {
    $cfg = require __DIR__ . '/config.php';
    if (!empty($cfg['db_host']) || !empty($cfg['DB_HOST'])) {
        echo '<h1>Jessie CMS is already installed.</h1><p><a href="/admin/">Go to Admin</a></p>';
        exit;
    }
}

$step = (int)($_GET['step'] ?? 1);
$error = '';
$success = '';

// Handle form submissions
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $step = (int)($_POST['step'] ?? 1);

    if ($step === 1) {
        // Requirements check — auto-advance
        $step = 2;
    }
    elseif ($step === 2) {
        // Database configuration
        $dbHost = trim($_POST['db_host'] ?? 'localhost');
        $dbPort = trim($_POST['db_port'] ?? '3306');
        $dbName = trim($_POST['db_name'] ?? '');
        $dbUser = trim($_POST['db_user'] ?? '');
        $dbPass = $_POST['db_pass'] ?? '';

        if (empty($dbName) || empty($dbUser)) {
            $error = 'Database name and username are required.';
            $step = 2;
        } else {
            // Test connection
            try {
                $dsn = "mysql:host={$dbHost};port={$dbPort};charset=utf8mb4";
                $pdo = new PDO($dsn, $dbUser, $dbPass, [PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION]);

                // Create database if it doesn't exist
                $pdo->exec("CREATE DATABASE IF NOT EXISTS `{$dbName}` CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci");
                $pdo->exec("USE `{$dbName}`");

                // Import schema
                $schemaFile = __DIR__ . '/database/schema.sql';
                if (file_exists($schemaFile)) {
                    $sql = file_get_contents($schemaFile);
                    $pdo->exec($sql);
                }


                // Write config.php
                $configContent = "<?php\n"
                    . "/**\n * Jessie CMS Configuration\n * Generated by installer on " . date('Y-m-d H:i:s') . "\n */\n\n"
                    . "// Database\n"
                    . "define('DB_HOST', " . var_export($dbHost, true) . ");\n"
                    . "define('DB_PORT', " . var_export($dbPort, true) . ");\n"
                    . "define('DB_NAME', " . var_export($dbName, true) . ");\n"
                    . "define('DB_USER', " . var_export($dbUser, true) . ");\n"
                    . "define('DB_PASS', " . var_export($dbPass, true) . ");\n"
                    . "define('DB_CHARSET', 'utf8mb4');\n\n"
                    . "// Paths\n"
                    . "define('CMS_ROOT', __DIR__);\n"
                    . "define('CMS_APP', CMS_ROOT . '/app');\n"
                    . "define('CMS_CONFIG', CMS_ROOT . '/config');\n"
                    . "define('CMS_UPLOADS', CMS_ROOT . '/public/uploads');\n\n"
                    . "// Security\n"
                    . "define('CMS_DEBUG', false);\n"
                    . "define('CMS_ENV', 'production');\n";

                file_put_contents(__DIR__ . '/config.php', $configContent);

                // Ensure extension tables exist (shop, dropshipping)
                if (!defined('CMS_ROOT')) { define('CMS_ROOT', __DIR__); }
                require_once __DIR__ . '/db.php';
                require_once __DIR__ . '/core/shop-coupons.php';
                require_once __DIR__ . '/core/shop-variants.php';
                require_once __DIR__ . '/core/shop-reviews.php';
                require_once __DIR__ . '/core/shop-digital.php';
                require_once __DIR__ . '/core/shop-wishlist.php';
                require_once __DIR__ . '/core/shop-analytics.php';
                require_once __DIR__ . '/core/shop-abandoned-carts.php';
                require_once __DIR__ . '/core/dropshipping.php';
                ShopCoupons::ensureTable();
                ShopVariants::ensureTable();
                ShopReviews::ensureTable();
                ShopDigital::ensureTable();
                ShopWishlist::ensureTable();
                ShopAnalytics::ensureTable();
                AbandonedCarts::ensureTable();
                Dropshipping::ensureTables();

                // Run plugin install scripts
                $pluginsDir = __DIR__ . '/plugins';
                if (is_dir($pluginsDir)) {
                    foreach (scandir($pluginsDir) as $pluginName) {
                        if ($pluginName === '.' || $pluginName === '..') continue;
                        $installFile = $pluginsDir . '/' . $pluginName . '/install.php';
                        if (file_exists($installFile)) {
                            try { require_once $installFile; } catch (\Throwable $e) {}
                        }
                    }
                }
                $_SESSION['install_db_ok'] = true;
                $step = 3;
            } catch (PDOException $e) {
                $error = 'Database connection failed: ' . $e->getMessage();
                $step = 2;
            }
        }
    }
    elseif ($step === 3) {
        // Admin account
        $username = trim($_POST['admin_user'] ?? '');
        $email = trim($_POST['admin_email'] ?? '');
        $password = $_POST['admin_pass'] ?? '';
        $siteName = trim($_POST['site_name'] ?? 'My Website');
        $siteUrl = trim($_POST['site_url'] ?? '');

        if (empty($username) || empty($password)) {
            $error = 'Username and password are required.';
            $step = 3;
        } elseif (strlen($password) < 8) {
            $error = 'Password must be at least 8 characters.';
            $step = 3;
        } else {
            try {
                require_once __DIR__ . '/config.php';
                $dsn = "mysql:host=" . DB_HOST . ";port=" . DB_PORT . ";dbname=" . DB_NAME . ";charset=utf8mb4";
                $pdo = new PDO($dsn, DB_USER, DB_PASS, [PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION]);

                // Create admin user
                $hash = password_hash($password, PASSWORD_DEFAULT);
                $stmt = $pdo->prepare("INSERT INTO admins (username, email, password_hash, role, created_at) VALUES (?, ?, ?, 'admin', NOW())
                    ON DUPLICATE KEY UPDATE password_hash = VALUES(password_hash), email = VALUES(email)");
                $stmt->execute([$username, $email ?: null, $hash]);

                // Save site settings
                $settings = [
                    'site_name' => $siteName,
                    'site_url' => $siteUrl ?: ('https://' . ($_SERVER['HTTP_HOST'] ?? 'localhost')),
                    'admin_email' => $email,
                    'setup_wizard_completed' => '0', // Will show setup wizard on first login
                ];
                foreach ($settings as $key => $value) {
                    $stmt = $pdo->prepare("INSERT INTO settings (`key`, `value`) VALUES (?, ?) ON DUPLICATE KEY UPDATE `value` = VALUES(`value`)");
                    $stmt->execute([$key, $value]);
                }

                $step = 4;
            } catch (PDOException $e) {
                $error = 'Setup failed: ' . $e->getMessage();
                $step = 3;
            }
        }
    }
}

